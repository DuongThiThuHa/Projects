package ss1_IntroductionToJava.thuchanh;

public class SuDungBien {
    public static void main(String[] args) {
        int i = 20;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Ha Noi";

        System.out.println("Bien i = " + i);
        System.out.println("Bien f = " + f);
        System.out.println("Bien d = " + d);
        System.out.println("Bien b = " + b);
        System.out.println("Bien c = " + c);
        System.out.println("Bien s = " + s);


    }
}
